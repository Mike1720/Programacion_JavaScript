// *OPERADORES DE COMPARACION
/* 
  NOTE:
    Se utilizan para comparar sus operando, y devuelve un valor logico en funcion si la comparacion es verdadera (true) o 
    falsa (false). Los operando pueden ser valores numericos, de cadena, logicos u objetos.
*/

// *MAYOR QUE
10 > 5 // -> true
10 > 10 // -> false

// *MENOR QUE
5 < 10 // -> true
10 < 5 // -> false

// *MAYOR IGUAL QUE
10 >= 10 // -> true
10 >= 5 // -> true
10 >= 15 // -> false

// *MENOR IGUAL QUE
10 <= 10 // -> true
10 <= 5 // -> false
10 <= 15 // -> true

// *IGUAL A
10 == 10 // -> true
10 == "10" // -> true
10 == 5 // -> false

// *ESTRICTAMENTE IGUAL
10 === 10 // -> true
10 === "10" // -> false
10 === 5 // -> false

// *DIFERENTE DE
10 != 10 // -> false
10 != "10" // -> false
10 != 5 // -> true

// *ESTRICTAMENTE DIFERENTE DE
10 !== 10 // -> false
10 !== "10" // -> true
10 !== 5 // -> true